-- Maintain cndp_notification_due_at = detected_at + 72h. Implemented as a
-- trigger because Postgres rejects this expression in a STORED GENERATED
-- column (timestamptz_pl_interval is STABLE, not IMMUTABLE).
CREATE OR REPLACE FUNCTION public.set_data_breach_cndp_due()
RETURNS trigger LANGUAGE plpgsql AS $$
BEGIN
  NEW.cndp_notification_due_at := NEW.detected_at + interval '72 hours';
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_data_breach_cndp_due ON public.data_breach_incidents;
CREATE TRIGGER trg_data_breach_cndp_due
BEFORE INSERT OR UPDATE OF detected_at ON public.data_breach_incidents
FOR EACH ROW EXECUTE FUNCTION public.set_data_breach_cndp_due();
