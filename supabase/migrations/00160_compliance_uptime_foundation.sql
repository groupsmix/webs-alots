-- Compliance + uptime monitoring foundation.
-- Adds Moroccan Law 09-08 operational tables and internal uptime event storage.

CREATE TABLE IF NOT EXISTS public.compliance_cndp (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  registration_number text UNIQUE,
  authorization_type text NOT NULL DEFAULT 'prior_authorization',
  authorization_number text,
  status text NOT NULL DEFAULT 'pending'
    CHECK (status IN ('pending', 'submitted', 'approved', 'rejected', 'renewal_required')),
  submitted_at timestamptz,
  approved_at timestamptz,
  expires_at timestamptz,
  renewal_due_at timestamptz,
  notes text,
  documents jsonb NOT NULL DEFAULT '[]'::jsonb,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS public.dsar_requests (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  dsar_number bigint GENERATED BY DEFAULT AS IDENTITY UNIQUE,
  requester_type text NOT NULL CHECK (requester_type IN ('patient', 'clinic_staff', 'external')),
  requester_name text NOT NULL,
  requester_email text NOT NULL,
  requester_phone text,
  clinic_id uuid REFERENCES public.clinics(id) ON DELETE SET NULL,
  request_type text NOT NULL CHECK (request_type IN ('access', 'rectification', 'deletion', 'portability', 'objection')),
  description text NOT NULL,
  status text NOT NULL DEFAULT 'received'
    CHECK (status IN ('received', 'in_progress', 'completed', 'rejected', 'extended')),
  legal_basis text,
  assigned_to uuid REFERENCES public.users(id) ON DELETE SET NULL,
  response_due_at timestamptz NOT NULL,
  responded_at timestamptz,
  response_notes text,
  created_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS public.data_breach_incidents (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  incident_number bigint GENERATED BY DEFAULT AS IDENTITY UNIQUE,
  detected_at timestamptz NOT NULL,
  -- Cannot use `GENERATED ALWAYS AS (detected_at + interval '72 hours') STORED`
  -- here: Postgres marks `timestamptz + interval` as STABLE (not IMMUTABLE) and
  -- rejects it for generated columns with SQLSTATE 42P17. The value is
  -- maintained by `trg_data_breach_cndp_due` below instead.
  cndp_notification_due_at timestamptz,
  cndp_notified_at timestamptz,
  severity text NOT NULL CHECK (severity IN ('low', 'medium', 'high', 'critical')),
  description text NOT NULL,
  affected_records_count integer,
  data_types_affected text[] NOT NULL DEFAULT '{}',
  clinics_affected uuid[] NOT NULL DEFAULT '{}',
  root_cause text,
  remediation_steps text,
  status text NOT NULL DEFAULT 'investigating'
    CHECK (status IN ('investigating', 'contained', 'notifying_cndp', 'cndp_notified', 'resolved')),
  created_by uuid REFERENCES public.users(id) ON DELETE SET NULL,
  created_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS public.consent_records (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id uuid REFERENCES public.users(id) ON DELETE SET NULL,
  clinic_id uuid REFERENCES public.clinics(id) ON DELETE SET NULL,
  consent_type text NOT NULL CHECK (consent_type IN (
    'terms_of_service',
    'privacy_policy',
    'health_data_processing',
    'marketing_communications',
    'whatsapp_notifications',
    'data_sharing_with_clinic'
  )),
  granted boolean NOT NULL,
  version text NOT NULL,
  ip_address text,
  user_agent text,
  created_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS public.data_retention_schedule (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  data_type text UNIQUE NOT NULL,
  retention_days integer NOT NULL,
  legal_basis text NOT NULL,
  last_purge_at timestamptz,
  next_purge_at timestamptz,
  records_purged_last_run integer NOT NULL DEFAULT 0
);

