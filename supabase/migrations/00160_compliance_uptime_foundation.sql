CREATE TABLE IF NOT EXISTS public.uptime_events (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  monitor_name text NOT NULL,
  monitor_url text,
  event_type text NOT NULL CHECK (event_type IN ('down', 'up', 'degraded')),
  message text,
  response_time_ms integer,
  occurred_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_uptime_events_occurred_at
  ON public.uptime_events(occurred_at DESC);

CREATE INDEX IF NOT EXISTS idx_uptime_events_monitor_name
  ON public.uptime_events(monitor_name, occurred_at DESC);

CREATE OR REPLACE VIEW public.uptime_sla_monthly AS
SELECT
  monitor_name,
  date_trunc('month', occurred_at)::date AS month,
  count(*) FILTER (WHERE event_type = 'down') AS downtime_events,
  count(*) FILTER (WHERE event_type = 'up') AS recovery_events,
  ROUND(
    (
      1 - (
        (count(*) FILTER (WHERE event_type = 'down'))::numeric * 60
      ) / NULLIF(
        extract(
          epoch FROM (
            (date_trunc('month', occurred_at) + interval '1 month')
            - date_trunc('month', occurred_at)
          )
        ),
        0
      )
    ) * 100,
    3
  ) AS uptime_pct
FROM public.uptime_events
GROUP BY monitor_name, date_trunc('month', occurred_at)::date;

