export type TranslationKey = keyof typeof translations.fr;

export function useEquipmentI18n(overrideLocale?: EquipmentLocale) {
  const [internalLocale, setLocale] = useState<EquipmentLocale>("fr");
  const locale = overrideLocale ?? internalLocale;

  const t = useCallback(
    (key: TranslationKey): string => {
      return translations[locale][key] ?? key;
    },
    [locale],
  );

  const isRtl = locale === "ar";

  const toggleLocale = useCallback(() => {
    setLocale((prev) => (prev === "fr" ? "ar" : "fr"));
  }, []);

  return { locale, setLocale, toggleLocale, t, isRtl };
}
